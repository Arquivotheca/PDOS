#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <stdarg.h>

static int tprintf(const char *format, ...);

int main(void)
{
    char buf[200];
    char *c;
    char *d;
    char *e;
    char *f;
    char *p;
    int frame;
    int cindex;
    int csects = 0;

    while (fgets(buf, sizeof buf, stdin) != NULL)
    {
        d = strstr(buf, "DCCPRLG");
        if (d != NULL)
        {
            frame = 0;
            e = strstr(buf, "ENTRY=YES");
            f = strstr(buf, "FRAME");
            if (f != NULL)
            {
                frame = atoi(f + 6);
            }
            cindex = 0;
            c = strstr(buf, "CINDEX");
            if (c != NULL)
            {
                cindex = atoi(c + 7);
            }
            if (e != NULL)
            {
                p = strchr(buf, '\t');
                if (p != NULL)
                {
                    *p = '\0';
                    tprintf("\tENTRY\t%s\n", buf);
                    *p = '\t';
                }
            }
            strcpy(d, "CSECT\n");
            tprintf(buf);
            tprintf("\tUSING\t*,12\n"); /*,7,8,9\n");*/
            tprintf("\tSAVE\t(14,12)");
            p = strchr(buf, '\t');
            if (p != NULL)
            {
                *p = '\0';
                tprintf(",,%s_&SYSDATE", buf);
                *p = '\t';
            }
            tprintf("\n");
            tprintf("\tLR\t12,15\n");
            
            tprintf("\tL\t15,76(13)\n");
            tprintf("\tST\t13,4(15)\n");
            tprintf("\tST\t15,8(13)\n");
            tprintf("\tLR\t13,15\n");
            tprintf("\tA\t15,=F'%d'\n", frame);
            tprintf("\tST\t15,76(13)\n");
        }
        else if ((d = strstr(buf, "DCCEPIL")) != NULL)
        {
            tprintf("\tL\t13,4(13)\n");
            strcpy(d, "RETURN (14,12),RC=(15)\n");
            tprintf(buf);
        }
        else if (strncmp(buf, "@FRAMESIZE_", 11) == 0)
        {
            tprintf("*");
            buf[10] = '@';
            tprintf(buf);
        }
        else if ((strstr(buf, "CSECT") != NULL)
                 && (strchr("@$", buf[0]) != NULL))
        {
            csects++;
            tprintf("*");
            tprintf(buf);
            if (csects == 1)
            {
                tprintf("\tCSECT\n");
            }
        }
        else
        {
            tprintf(buf);
        }
    }
    return (0);
}

static int tprintf(const char *format, ...)
{
    va_list arg;
    char buf[300];
    size_t icnt;
    size_t ocnt;

    va_start(arg, format);
    vsprintf(buf, format, arg);
    va_end(arg);
    ocnt = icnt = 0;
    while (buf[icnt] != '\0')
    {
        if (buf[icnt] == '\t')
        {
            if (ocnt < 9)
            {
                for (; ocnt < 9; ocnt++)
                {
                    putchar(' ');
                }
            }
            else if (ocnt < 15)
            {
                for (; ocnt < 15; ocnt++)
                {
                    putchar(' ');
                }
            }
            else
            {
                putchar(' ');
            }
        }
        else
        {
            putchar(buf[icnt]);
            ocnt++;
        }
        icnt++;
    }
    return (ocnt);
}

#include <stdio.h>
#include <stdlib.h>
#include <string.h>

int main(void)
{
    char buf[200];
    char *c;
    char *d;
    char *e;
    char *f;
    int frame;
    int cindex;

    while (fgets(buf, sizeof buf, stdin) != NULL)
    {
        d = strstr(buf, "DCCPRLG");
        if (d != NULL)
        {
            frame = 0;
            e = strstr(buf, "ENTRY");
            f = strstr(buf, "FRAME");
            if (f != NULL)
            {
                frame = atoi(f + 6);
            }
            cindex = 0;
            c = strstr(buf, "CINDEX");
            if (c != NULL)
            {
                cindex = atoi(c + 7);
            }
            if (e != NULL)
            {
                strcpy(d, "CSECT\n");
            }
            else
            {
                strcpy(d, "EQU\t*\n");
            }
            fputs(buf, stdout);
            printf("\tUSING\t*,12\n");
            printf("\tSAVE\t(14,12)\n");
            printf("\tLR\t12,15\n");
            printf("\tL\t15,76(13)\n");
            printf("\tST\t15,4(15)\n");
            printf("\tST\t15,8(13)\n");
            printf("\tLR\t13,15\n");
            printf("\tA\t15,@FRAMESIZE@%d\n", cindex);
            printf("\tST\t15,76(13)\n");
        }
        else if ((d = strstr(buf, "DCCEPIL")) != NULL)
        {
            printf("\tL\t13,4(13)\n");
            strcpy(d, "RETURN (14,12),RC=(15)\n");
            fputs(buf, stdout);
        }
        else if (strncmp(buf, "@FRAMESIZE_", 11) == 0)
        {
            memcpy(buf, "@FRAMESIZE@", 11);
            fputs(buf, stdout);
        }
        else
        {
            fputs(buf, stdout);
        }
    }
    return (0);
}
